package org.firstinspires.ftc.teamcode.mechanisms;

/**
 * This is the Follower action enumeration set. The Follower can only work on these individual
 * predefined set of actions in sequence when they are chosen at runtime time.
 *
 */
public enum FollowerAction {
    CLOSE_LAUNCH, FAR_LAUNCH, GRAB_HIGH_SPIKE, GRAB_MID_SPIKE, GRAB_LOW_SPIKE,
    OPEN_GATE, STOP
}